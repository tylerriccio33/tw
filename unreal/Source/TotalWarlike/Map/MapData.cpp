bool ReadJsonArray(const FString& Path, TArray<TSharedPtr<FJsonValue>>& Out, FString& OutError)
{
    FString Text;
    if (!FFileHelper::LoadFileToString(Text, *Path))
    {
        OutError = FString::Printf(TEXT("cannot read %s"), *Path);
        return false;
    }
    const TSharedRef<TJsonReader<>> Reader = TJsonReaderFactory<>::Create(Text);
    if (!FJsonSerializer::Deserialize(Reader, Out))
    {
        OutError = FString::Printf(TEXT("malformed JSON array in %s"), *Path);
        return false;
    }
    return true;
}

bool ReadJsonObject(const FString& Path, TSharedPtr<FJsonObject>& Out, FString& OutError)
{
    FString Text;
    if (!FFileHelper::LoadFileToString(Text, *Path))
    {
        OutError = FString::Printf(TEXT("cannot read %s"), *Path);
        return false;
    }
    const TSharedRef<TJsonReader<>> Reader = TJsonReaderFactory<>::Create(Text);
    if (!FJsonSerializer::Deserialize(Reader, Out) || !Out.IsValid())
    {
        OutError = FString::Printf(TEXT("malformed JSON object in %s"), *Path);
        return false;
    }
    return true;
}

/// `[x, y, z]` -> FVector. The baker already emits Unreal centimetres.
bool ReadPoint(const TSharedPtr<FJsonValue>& Value, FVector& Out)
{
    const TArray<TSharedPtr<FJsonValue>>* Triple = nullptr;
    if (!Value.IsValid() || !Value->TryGetArray(Triple) || Triple->Num() < 3)
    {
        return false;
    }
    Out = FVector((*Triple)[0]->AsNumber(), (*Triple)[1]->AsNumber(), (*Triple)[2]->AsNumber());
    return true;
}

/// A deliberately minimal Wavefront reader: it handles exactly what
/// `bake/src/main.rs` writes (`v`, `vn`, and triangular `f a//a b//b c//c`) and
/// rejects anything else rather than guessing. The file is generated by code in
/// this repo, so the general case is not worth carrying.
bool ParseObj(const FString& Path, TArray<FVector>& OutVerts, TArray<FVector>& OutNormals,
              TArray<int32>& OutIndices, FString& OutError)
{
    TArray<FString> Lines;
    if (!FFileHelper::LoadFileToStringArray(Lines, *Path))
    {
        OutError = FString::Printf(TEXT("cannot read %s"), *Path);
        return false;
    }

    OutVerts.Reset();
    OutNormals.Reset();
    OutIndices.Reset();

    for (const FString& Line : Lines)
    {
        if (Line.StartsWith(TEXT("v ")))
        {
            TArray<FString> Parts;
            Line.ParseIntoArray(Parts, TEXT(" "), true);
            if (Parts.Num() < 4)
            {
                OutError = FString::Printf(TEXT("bad vertex in %s: %s"), *Path, *Line);
                return false;
            }
            OutVerts.Emplace(FCString::Atod(*Parts[1]), FCString::Atod(*Parts[2]),
                             FCString::Atod(*Parts[3]));
        }
        else if (Line.StartsWith(TEXT("vn ")))
        {
            TArray<FString> Parts;
            Line.ParseIntoArray(Parts, TEXT(" "), true);
            if (Parts.Num() < 4)
            {
                OutError = FString::Printf(TEXT("bad normal in %s: %s"), *Path, *Line);
                return false;
            }
            OutNormals.Emplace(FCString::Atod(*Parts[1]), FCString::Atod(*Parts[2]),
                               FCString::Atod(*Parts[3]));
        }
        else if (Line.StartsWith(TEXT("f ")))
        {
            TArray<FString> Parts;
            Line.ParseIntoArray(Parts, TEXT(" "), true);
            if (Parts.Num() != 4)
            {
                OutError = FString::Printf(TEXT("%s has a non-triangular face: %s"), *Path, *Line);
                return false;
            }
            for (int32 i = 1; i <= 3; ++i)
            {
                FString Vertex = Parts[i];
                int32 Slash = INDEX_NONE;
                if (Vertex.FindChar(TEXT('/'), Slash))
                {
                    Vertex.LeftInline(Slash);
                }
                // OBJ indices are 1-based.
                OutIndices.Add(FCString::Atoi(*Vertex) - 1);
            }
        }
    }

    if (OutVerts.Num() == 0 || OutIndices.Num() == 0)
    {
        OutError = FString::Printf(TEXT("%s has no geometry"), *Path);
        return false;
    }
    if (OutNormals.Num() != 0 && OutNormals.Num() != OutVerts.Num())
    {
        // The baker writes one normal per vertex, in step. Anything else means
        // the face indices below would silently pair the wrong two.
        OutError = FString::Printf(TEXT("%s has %d normals for %d vertices"), *Path,
                                   OutNormals.Num(), OutVerts.Num());
        return false;
    }
    return true;
}

